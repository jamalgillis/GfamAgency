async function main() {
  // Get the Convex URL from environment
  const convexUrl = process.env.CONVEX_URL || process.env.NEXT_PUBLIC_CONVEX_URL;

  if (!convexUrl) {
    console.error("‚ùå CONVEX_URL or NEXT_PUBLIC_CONVEX_URL not found in environment");
    console.log("Make sure you have a .env.local file with NEXT_PUBLIC_CONVEX_URL set");
    process.exit(1);
  }

  console.log("üöÄ Starting Convex seed...");
  console.log(`üìç Convex URL: ${convexUrl}`);
  console.log(`üì¶ Services to seed: ${servicesData.length}`);

  const client = new ConvexHttpClient(convexUrl);

  try {
    const result = await client.mutation(api.seed.seedServices, {
      services: servicesData,
      clearExisting: true,
    });

    console.log("‚úÖ Seed completed successfully!");
    console.log(`   Inserted: ${result.inserted} services`);
    console.log(`   Message: ${result.message}`);
  } catch (error) {
    console.error("‚ùå Seed failed:", error);
    process.exit(1);
  }
}

main();
